CPL P1.1
AJMP TIMER0

ORG 0x80
TIMER0: 
DJNZ R7, RETURN
CLR TR0
MOV A, @R0
MOV R7, A
INC R0
MOV A, @R0
JZ RELOAD
SJMP NXT
RELOAD: 
MOV R0, #0X60
NXT:
CPL P1.0
CPL P1.2
MOV TH0, #0X00
MOV TL0, #0X00
SETB TR0

RETURN: RETI 

ORG 0xB0
MAIN:
